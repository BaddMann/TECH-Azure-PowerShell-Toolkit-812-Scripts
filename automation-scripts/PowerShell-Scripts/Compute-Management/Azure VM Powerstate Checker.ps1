    [ValidateNotNullOrEmpty()]
    [string]$WEResourceGroupName,
    
    [Parameter(Mandatory=$true)]
    [string]$WEVmName
)

Write-WELog " Checking power state for VM: $WEVmName" " INFO"
; 
